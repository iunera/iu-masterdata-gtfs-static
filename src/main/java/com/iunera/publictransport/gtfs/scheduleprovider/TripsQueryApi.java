import java.util.List;
import java.util.Map;
import org.springframework.beans.factory.annotation.Autowired;
import org.springframework.http.HttpStatus;
import org.springframework.http.ResponseEntity;
import org.springframework.jdbc.core.JdbcTemplate;
import org.springframework.web.bind.annotation.CrossOrigin;
import org.springframework.web.bind.annotation.RequestMapping;
import org.springframework.web.bind.annotation.RequestMethod;
import org.springframework.web.bind.annotation.RestController;

@RestController
@CrossOrigin
@RequestMapping("/apiv1")
public class TripsQueryApi {

  @Autowired private JdbcTemplate jdbcTemplate;

  @RequestMapping(value = "/trips_count", method = RequestMethod.GET)
  public ResponseEntity<String> countTrips() {

    String sql = "SELECT * from trips;";

    List<Map<String, Object>> rows = jdbcTemplate.queryForList(sql);

    String resp = Integer.toString(rows.size());

    return new ResponseEntity<>(resp, HttpStatus.FOUND);
  }
}
